import scipy.stats as stats

# 示例数据
accs1 = [0.7259, 0.7679, 0.7155, 0.7485, 0.7823, 0.7581, 0.8040, 0.7633, 0.7463, 0.7883]
accs2 = [0.5655, 0.5569, 0.5623, 0.5519, 0.5655, 0.5591, 0.5486, 0.5612, 0.5640 , 0.5515]

# 进行独立样本t检验
t_stat, p_value = stats.ttest_ind(accs1, accs2)

print(f't统计量: {t_stat}')
print(f'p值: {p_value}')

# 判断p值是否小于0.05，如果是，则通常认为两组数据存在显著差异
if p_value < 0.05:
    print("两组数据存在显著差异。")
else:
    print("两组数据不存在显著差异。")
